let g = prompt("Type a variety of fruits")
switch (g){
    case "BANANA": {
        console.log("Banana is good!")
    }
    break;
    case "ORANGE": {
        console.log("I am not a fan of orange")
    }
    break;
    case "APPLE": {
        console.log("How you like them apples?")
    }
    break;
    default: {
        console.log("I have never heard of that fruit.")
    }
}